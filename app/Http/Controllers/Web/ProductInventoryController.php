    {
        $product = Product::where([
            "id" => $id,
            "admin_id" => Auth::id()
        ])->with("inventory")->first();

        if (!$product) {
            return redirect("/products");
        }

        return view("product-inventory", ["prod" => $product->toArray()]);
    }

