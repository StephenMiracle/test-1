     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $orders = Order::withProductInfo(Auth::id());
        $total = 0;

        foreach ($orders as $o) {
            $total += $o->total_cents;
        }


        return view("orders", [
            "orders" => $orders,
            "total" => number_format($total / 100),
            "avg" => $total / 100 / count($orders)
        ]);
    }

